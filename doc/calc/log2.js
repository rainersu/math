/**
* 计算数字 `num` 以 `2` 为底的对数。
* @see {@link Arith.log10}
* @see {@link Arith.log1p}
* @see {@link https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/Math/log2|Math.log2}
* @access public
* @func Arith.log2
* @param {number} num - 数字。
* @returns {number}
* @example
* console.log(Arith.log2(3));      //   1.584962500721156
* console.log(Arith.log2(2));      //   1
* console.log(Arith.log2(1));      //   0
* console.log(Arith.log2(0));      // -Infinity
* console.log(Arith.log2(-2));     //  NaN
* console.log(Arith.log2(1024));   //  10
*/